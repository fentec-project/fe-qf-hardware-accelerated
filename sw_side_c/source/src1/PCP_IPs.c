/**************************************************************************************************************/
ElementFp12 hw_optimalAte_pairing_calc(ElementFp4 G1, ElementFp2 G2, u8 pcp_core_id) {
	ElementFp2 XQ, YQ, YmQ, ZQ;
	pcp_eql_fp(G1.d0.d0, XQ.d0, pcp_core_id);
	pcp_eql_fp(G1.d0.d1, XQ.d1, pcp_core_id);
	pcp_eql_fp(G1.d1.d0, YQ.d0, pcp_core_id);
	pcp_eql_fp(G1.d1.d1, YQ.d1, pcp_core_id);
	pcp_neg_fp2(YQ, &YmQ, pcp_core_id);
	pcp_eql_fp(unit, ZQ.d0, pcp_core_id);
	pcp_eql_fp(zero, ZQ.d1, pcp_core_id);

	ElementFp2 XT = XQ, YT = YQ, ZT = ZQ;
	ElementFp2 XQ1, YQ1, ZQ1;
	ElementFp2 XmQ2, YmQ2, ZmQ2;
	ElementFp12 f, ell;

	pcp_eql_fp(unit, f.d0.d0.d0, pcp_core_id);
	pcp_eql_fp(zero, f.d0.d0.d1, pcp_core_id);
	pcp_eql_fp(zero, f.d0.d1.d0, pcp_core_id);
	pcp_eql_fp(zero, f.d0.d1.d1, pcp_core_id);
	pcp_eql_fp(zero, f.d0.d2.d0, pcp_core_id);
	pcp_eql_fp(zero, f.d0.d2.d1, pcp_core_id);
	pcp_eql_fp(zero, f.d1.d0.d0, pcp_core_id);
	pcp_eql_fp(zero, f.d1.d0.d1, pcp_core_id);
	pcp_eql_fp(zero, f.d1.d1.d0, pcp_core_id);
	pcp_eql_fp(zero, f.d1.d1.d1, pcp_core_id);
	pcp_eql_fp(zero, f.d1.d2.d0, pcp_core_id);
	pcp_eql_fp(zero, f.d1.d2.d1, pcp_core_id);

	for (int i = 1; i < len_s; i++) {
		pcp_sqr_fp12(f, &f, pcp_core_id);
		pcp_point_doubling_line_evaluation(XT, YT, ZT, G2.d0, G2.d1, &XT, &YT, &ZT, &ell, pcp_core_id);
		pcp_mul_fp12(f, ell, &f, pcp_core_id);

		if (s[i] == -1) {
			pcp_point_addition_line_evaluation(XQ, YmQ, ZQ, XT, YT, ZT, G2.d0, G2.d1, &XT, &YT, &ZT, &ell, pcp_core_id);
			pcp_mul_fp12(f, ell, &f, pcp_core_id);
		}
		else if(s[i] == 1) {
			pcp_point_addition_line_evaluation(XQ, YQ, ZQ, XT, YT, ZT, G2.d0, G2.d1, &XT, &YT, &ZT, &ell, pcp_core_id);
			pcp_mul_fp12(f, ell, &f, pcp_core_id);
		}
	}

	pcp_Q1_mQ2_point_computation(XQ, YQ, ZQ, &XQ1, &YQ1, &ZQ1, &XmQ2, &YmQ2, &ZmQ2, pcp_core_id);
	pcp_point_addition_line_evaluation(XQ1, YQ1, ZQ1, XT, YT, ZT, G2.d0, G2.d1, &XT, &YT, &ZT, &ell, pcp_core_id);
	pcp_mul_fp12(f, ell, &f, pcp_core_id);
	pcp_point_addition_line_evaluation(XmQ2, YmQ2, ZmQ2, XT, YT, ZT, G2.d0, G2.d1, &XT, &YT, &ZT, &ell, pcp_core_id);
	pcp_mul_fp12(f, ell, &f, pcp_core_id);

	pcp_final_exponentiation(f, &f, pcp_core_id);

	return f;
}

