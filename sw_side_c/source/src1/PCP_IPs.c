void pcp_inv_r2l_fp (u8 *a, u8 *c, u8 pcp_core_id) { // Fermat's Little Theorem: a ^ (p-2) mod p; based on R2L ME algorithm
	u8 tmp[] =
	"\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00";

	pcp_eql_fp(unit, c, pcp_core_id);
	pcp_eql_fp(a, tmp, pcp_core_id);
	for (int i = (len_exp_pm2 - 1); i >= 0; i--) {
		if(exp_pm2[i] == 1)
			pcp_mul_fp(c, tmp, c, pcp_core_id);
		pcp_mul_fp(tmp, tmp, tmp, pcp_core_id);
	}
}

